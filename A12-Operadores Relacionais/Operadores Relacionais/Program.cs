int x = 1;
int y = 2;

Console.WriteLine($"x == y:{x == y}"); //x é igual a y ? -> falso
System.Console.WriteLine($"x != y:{x != y}"); // x é diferente de y ? -> verdadeiro
System.Console.WriteLine($"x < y:{x < y}"); // verdadeiro
System.Console.WriteLine($"x > y:{x > y}"); // falso
System.Console.WriteLine($"x <= y :{x <= y}"); // verdadeiro
System.Console.WriteLine($"x >= y :{x >= y}"); // falso
