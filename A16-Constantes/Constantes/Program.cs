//Calculo do comprimento do círculo
const double PI = 3.1450;
System.Console.WriteLine("#Calculo do Comprimento do Círculo");
Console.WriteLine("Digite o raio do círculo");
double raio = Convert.ToDouble(Console.ReadLine());
double comprimentoDoCírculo = 2*PI*raio;
System.Console.WriteLine("O Comprimento do círculo é:1 " + comprimentoDoCírculo);
